// ‚îÄ‚îÄ‚îÄ Locale Context ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
interface LocaleContextValue {
    locale: Locale;
    setLocale: (l: Locale) => void;
    t: (key: string) => string;
}

export const LocaleContext = createContext<LocaleContextValue>({
    locale: 'es',
    setLocale: () => { },
    t: (key) => key,
});

export function LocaleProvider({ children }: { children: React.ReactNode }) {
    const [locale, setLocaleState] = useState<Locale>(() => {
        try {
            const stored = localStorage.getItem('locale') as Locale;
            if (dict[stored]) return stored;
        } catch { }
        return 'es';
    });

    const setLocale = useCallback((l: Locale) => {
        setLocaleState(l);
        try { localStorage.setItem('locale', l); } catch { }
    }, []);

    const tFn = useCallback((key: string): string => {
        return dict[locale]?.[key] ?? dict['es']?.[key] ?? key;
    }, [locale]);

    return (
        <LocaleContext.Provider value={{ locale, setLocale, t: tFn }}>
            {children}
        </LocaleContext.Provider>
    );
}

/** Hook to use translations in any component */
export function useLocale() {
    return useContext(LocaleContext);
}

/** Standalone translate function (reads from localStorage ‚Äî for non-React contexts) */
export function t(key: string, locale?: Locale): string {
    const loc = locale ?? (() => {
        try { return (localStorage.getItem('locale') as Locale) || 'es'; } catch { return 'es'; }
    })();
    return dict[loc]?.[key] ?? dict['es']?.[key] ?? key;
}

export function getLocale(): Locale {
    try {
        const stored = localStorage.getItem('locale') as Locale;
        if (dict[stored]) return stored;
    } catch { }
    return 'es';
}
